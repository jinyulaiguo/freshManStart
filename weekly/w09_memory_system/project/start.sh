#!/bin/bash
# ==========================================================================
# Day 63 一键拉起 Web Dashboard 与后端服务脚本
# ==========================================================================

# 解决脚本在不同目录下执行时的寻址问题，强制切换到当前脚本所在的 project 目录
CDPATH= cd -- "$(dirname -- "$0")"

# 物理定位并激活工作区已建立好的 Python 虚拟环境
VENV_PATH="../../../.venv/bin/activate"

if [ -f "$VENV_PATH" ]; then
    echo "=========================================================="
    echo "⚙️  正在激活工作区虚拟环境并导入依赖环境..."
    source "$VENV_PATH"
else
    echo "⚠️  未能在预设路径找到虚拟环境: $VENV_PATH"
    echo "将尝试使用系统默认的 python 环境..."
fi

# 检查 8000 端口是否被占用，防止二次启动冲突
PORT=8000
PID=$(lsof -t -i:$PORT)
if [ ! -z "$PID" ]; then
    echo "⚠️  检测到端口 $PORT 已被进程 (PID: $PID) 占用。正在尝试终止它..."
    kill -9 $PID
    sleep 1
fi

echo "=========================================================="
echo "🚀  正在拉起多层级记忆增强 Agent Web 服务..."
echo "📊  服务加载成功后，请在浏览器中打开链接访问调试看板："
echo "👉  http://127.0.0.1:8000/"
echo "=========================================================="
echo "停止服务请在控制台按：Ctrl + C"
echo "=========================================================="

# 物理拉起 FastAPI 后端
python server.py
